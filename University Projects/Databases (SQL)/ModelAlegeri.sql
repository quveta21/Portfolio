CREATE OR ALTER PROCEDURE AflaCastigator @ID_SECTIE INT,
@ID_CASTIGATOR INT OUTPUT
AS
BEGIN

DECLARE @MAX INT;

SELECT TOP 1 @ID_CASTIGATOR = V.ID_CANDIDAT, @MAX = V.TOTAL_VOTURI FROM
(SELECT ID_CANDIDAT, COUNT(*) AS TOTAL_VOTURI FROM VOTURI
WHERE ID_SECTIE = @ID_SECTIE
GROUP BY ID_CANDIDAT) V
ORDER BY V.ID_CANDIDAT;

DECLARE @TOTAL INT;
SELECT @TOTAL = COUNT(*) FROM VOTURI
WHERE ID_SECTIE = @ID_SECTIE;

DECLARE @PROCENT REAL;
SET @PROCENT = (@MAX * 100) / @TOTAL;

RETURN @PROCENT;

END;


CREATE OR ALTER VIEW VW_VOTURI_GEORGESCU AS 
SELECT CONVERT(VARCHAR(10),S.NUMAR) + ': ' + CONVERT(VARCHAR(10), COUNT(*)) AS [DATE] FROM VOTURI V
INNER JOIN CANDIDATI C ON V.ID_CANDIDAT = C.ID
INNER JOIN SECTII S ON V.ID_SECTIE= S.ID
WHERE C.PRENUME LIKE 'Calin' AND C.NUME = 'Georgescu'
GROUP BY V.ID_SECTIE, S.NUMAR
HAVING COUNT(*) > 500