CREATE DATABASE ModelAlegeri
GO
USE ModelAlegeri
GO

CREATE TABLE SECTII
(
	ID INT PRIMARY KEY IDENTITY,
	NUMAR INT
);

CREATE TABLE CETATENI
(
	ID INT PRIMARY KEY IDENTITY,
	NUME VARCHAR(50),
	PRENUME VARCHAR(50),
	CNP VARCHAR(20),
	ID_SECTIE INT FOREIGN KEY REFERENCES SECTII(ID)
);

CREATE TABLE CANDIDATI
(
	ID INT PRIMARY KEY IDENTITY,
	NUME VARCHAR(50),
	PRENUME VARCHAR(50),
	DATA_NASTERII DATE
);

CREATE TABLE VOTURI
(
	ID INT PRIMARY KEY IDENTITY,
	ID_CETATEAN INT FOREIGN KEY REFERENCES CETATENI(ID),
	ID_SECTIE INT FOREIGN KEY REFERENCES SECTII(ID),
	ORA TIME,
	PE_LISTA_SUPLIMENTARA BIT,
	ID_CANDIDAT INT FOREIGN KEY REFERENCES CANDIDATI(ID)
);

CREATE TABLE ECHIPAJE
(
	ID INT PRIMARY KEY IDENTITY,
	NUMAR INT,
	NUMAR_MEMBRII INT
);

CREATE TABLE ECHIPAJE_SECTII
(
	ID_ECHIPAJ INT FOREIGN KEY REFERENCES ECHIPAJE(ID),
	ID_SECTIE INT FOREIGN KEY REFERENCES SECTII(ID),
	CONSTRAINT PK_ES PRIMARY KEY (ID_ECHIPAJ, ID_SECTIE)
);

